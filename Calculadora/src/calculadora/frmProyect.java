    private void btndato16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndato16ActionPerformed
         String cadena;
         cadena = btndato.getText();
         
         if (cadena.length() <= 0) {
             btndato.setText("0.");
         }
         else{
             if (!existepunto(btndato.getText()))
                 btndato.setText(btndato.getText()+".");
         }
    }//GEN-LAST:event_btndato16ActionPerformed

    public static boolean existepunto(String cadena) {
        boolean resultado;
        resultado = false;
        
        for (int i = 0; i < cadena.length(); i++) {
            if (cadena.substring(1, i+1).equals(".")){
                resultado = true;
                break;
            }
            
        }
        return resultado;
    }
    
