    /* Se aplica el patron singleton en el metodo calculo porque es un metodo
    unico que realiza todas las funciones ademas de que indica al codigo de solo
    abrir una calculadora en vez de varias, la informacion usada en la calculadora
    es guardada en el cache del IDE para ser uasada antes de realizar una
    operacion con el boton igual (=).
    */
    public static String calculo(String dato1,String dato2, String signo){
        double resultado = 0.0;
        String respuesta;
        
        if (signo.equals("-")){
            resultado = Double.parseDouble(dato1)-Double.parseDouble(dato2);
            
        }
        if (signo.equals("+")){
            resultado = Double.parseDouble(dato1)+Double.parseDouble(dato2);
            
        }
        if (signo.equals("*")){
            resultado = Double.parseDouble(dato1)*Double.parseDouble(dato2);
            
        }
        if (signo.equals("/")){
            resultado = Double.parseDouble(dato1)/Double.parseDouble(dato2);
            
        }
        respuesta=String.valueOf(resultado);
        return respuesta;       }
    
    
